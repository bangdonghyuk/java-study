package com.bang.variable;

public class sumAge {
	public static void main(String[] args) {
		
		int a = 10; // 영희 나이
		
		int b = 20; // 철수 나이
		
		int age = a + b; // 합계나이
		
		System.out.println("영희의 나이는" + a + "세입니다.");
		System.out.println("철수의 나이는" + b + "세입니다.");
		System.out.println("영희와 철수의 나의 합은" + age + "세입니다.");
		
		int youngHeeAge; // 선언
		youngHeeAge = 10; // 사용
		
		int chulSuAge;
		chulSuAge = 20;
		
		int result;
		result = youngHeeAge + chulSuAge;
		
		System.out.println("영희와 철수 나이의 합은 [" + result + "]입니다.");
		System.out.println("전체선택 후 ctrl + shift + f 코드정리");
		System.out.println("ctrl + D 한줄삭제");
	}
}
